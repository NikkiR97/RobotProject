class MapSpot:

    def __init__(self):
        self.detected = False
        self.traveled = False
        self.obstacle = False

    def setDetected(detected):
        self.detected = detected

    def getDetected():
        return self.detected

    def setTraveled(traveled):
        self.traveled = traveled

    def getTraveled():
        return self.traveled

    def setObstacle(obstacle):
        self.obstacle = obstacle

    def getObstacle():
        return self.obstacle